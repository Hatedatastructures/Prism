    TEST(RealityResponseDeep, BuildShCipherAndCompression)
    {
        tls::hello_features ch;
        ch.session_id.clear();

        std::array<std::uint8_t, 32> eph_pub{};
        auto body = build_server_hello_body(ch, eph_pub);

        // Offset after version(2)+random(32)+sid_len(1)+sid(0)=35
        // cipher at 35,36; compression at 37
        const auto cipher = (static_cast<std::uint16_t>(body[35]) << 8) | body[36];
        EXPECT_TRUE(cipher == tls::CIPHER_AES_128_GCM_SHA256) << "build_sh: cipher=0x1301";
        EXPECT_TRUE(body[37] == 0x00) << "build_sh: compression=0";
    }

    TEST(RealityResponseDeep, BuildShExtensions)
    {
        tls::hello_features ch;
        ch.session_id.clear();

        std::array<std::uint8_t, 32> eph_pub{};
        for (std::size_t i = 0; i < 32; ++i)
            eph_pub[i] = static_cast<std::uint8_t>(i);

        auto body = build_server_hello_body(ch, eph_pub);

        // extensions_length at offset 38 (no sid)
        const auto ext_len = (static_cast<std::uint16_t>(body[38]) << 8) | body[39];
        EXPECT_TRUE(ext_len > 0) << "build_sh ext: len > 0";

        // supported_versions ext: type 0x002B at offset 40
        const auto ext_type1 = (static_cast<std::uint16_t>(body[40]) << 8) | body[41];
        EXPECT_TRUE(ext_type1 == tls::EXT_SUPPORTED_VERSIONS) << "build_sh ext: supported_versions";

        // key_share ext: type 0x0033 after supported_versions
        // supported_versions: type(2) + len(2) + data(2) = 6 bytes
        const auto ext_type2 = (static_cast<std::uint16_t>(body[46]) << 8) | body[47];
        EXPECT_TRUE(ext_type2 == tls::EXT_KEY_SHARE) << "build_sh ext: key_share";

        // key_share should contain eph_pub
        // key_share ext: type(2) + len(2) + group(2) + key_len(2) + key(32)
        // supported_versions ends at 45, key_share type at 46-47, ext_len at 48-49
        // ext body starts at 50: group(50-51), key_len(52-53), key_data(54+)
        const auto group = (static_cast<std::uint16_t>(body[50]) << 8) | body[51];
        EXPECT_TRUE(group == tls::GROUP_X25519) << "build_sh ext: group=X25519";
        const auto key_len = (static_cast<std::uint16_t>(body[52]) << 8) | body[53];
        EXPECT_TRUE(key_len == 32) << "build_sh ext: key_len=32";
        EXPECT_TRUE(body[54] == 0x00) << "build_sh ext: eph_pub[0]";
        EXPECT_TRUE(body[85] == 0x1F) << "build_sh ext: eph_pub[31]";
    }

