    TEST(RealityResponseDeep, BuildCertVerifyStructure)
    {
        psm::crypto::ed25519_keypair kp{};
        ED25519_keypair(kp.public_key.data(), kp.private_key.data());

        std::array<std::uint8_t, 32> transcript{};
        for (std::size_t i = 0; i < 32; ++i)
            transcript[i] = static_cast<std::uint8_t>(i);

        auto body = build_certificate_verify(kp, transcript);
        // sig_scheme(2) + sig_len(2) + signature(64) = 68
        EXPECT_TRUE(body.size() == 68) << "build_cv: size=68";
        // SIG_ED25519 = 0x0807
        EXPECT_TRUE(body[0] == 0x08 && body[1] == 0x07) << "build_cv: scheme=0x0807";
        // sig_len = 64 = 0x0040
        EXPECT_TRUE(body[2] == 0x00 && body[3] == 0x40) << "build_cv: sig_len=64";
    }

