    // ─── encrypt_record 完整往返 ────────────────────

    TEST(RealityResponseDeep, EncryptDecryptRoundtrip)
    {
        // 加密后再解密，验证往返正确性
        std::array<std::uint8_t, 16> key{};
        for (std::size_t i = 0; i < 16; ++i)
            key[i] = static_cast<std::uint8_t>(i + 1);
        std::array<std::uint8_t, 12> iv{};
        for (std::size_t i = 0; i < 12; ++i)
            iv[i] = static_cast<std::uint8_t>(i + 1);

        const std::uint8_t plaintext[] = {0xDE, 0xAD, 0xBE, 0xEF};

        encrypt_params params{key, iv, 0, tls::CT_HANDSHAKE, plaintext};
        auto [ec, encrypted] = encrypt_record(params);
        EXPECT_TRUE(ec == psm::fault::code::success) << "encrypt roundtrip: success";

        // 手动解密验证
        // encrypted = record_header(5) + ciphertext_with_tag
        const auto enc_payload_len = encrypted.size() - 5;
        // inner = plaintext + content_type + tag
        // so plaintext.size() + 1 + 16 = enc_payload_len
        EXPECT_TRUE(enc_payload_len == sizeof(plaintext) + 1 + 16)
                     << "encrypt roundtrip: payload len correct";

        // 用 AEAD open 解密
        auto nonce = psm::stealth::common::aead_nonce(iv, 0);
        auto ad = psm::stealth::common::record_ad(static_cast<std::uint16_t>(enc_payload_len));

        psm::crypto::aead_context aead(psm::crypto::aead_cipher::aes_128_gcm, key);
        psm::memory::vector<std::uint8_t> decrypted(sizeof(plaintext) + 1);

        auto open_ec = aead.open(psm::crypto::open_input{
            decrypted,
            std::span<const std::uint8_t>(encrypted.data() + 5, enc_payload_len),
            std::span<const std::uint8_t>{nonce.data(), nonce.size()},
            std::span<const std::uint8_t>{ad.data(), ad.size()}});

        EXPECT_TRUE(!psm::fault::failed(open_ec)) << "encrypt roundtrip: decrypt success";
        EXPECT_TRUE(decrypted.size() == sizeof(plaintext) + 1) << "encrypt roundtrip: decrypted size";
        // 明文部分
        EXPECT_TRUE(decrypted[0] == 0xDE) << "encrypt roundtrip: plaintext[0]";
        EXPECT_TRUE(decrypted[3] == 0xEF) << "encrypt roundtrip: plaintext[3]";
        // content_type 尾部
        EXPECT_TRUE(decrypted[4] == tls::CT_HANDSHAKE) << "encrypt roundtrip: trailing content_type";
    }

